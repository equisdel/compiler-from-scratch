package TercerEtapa;

public class TipoPair {

        private String tipo;
        private String c1;
        private String c2;

        public TipoPair(String tipo, String c1, String c2) {
            this.tipo = tipo;
            this.c1 = c1;
            this.c2 = c2;
        }

        public String getTipo() {
            return this.tipo;
        }
}
