package TercerEtapa;

public class GotoPair {

        int Tid;
        String Tscope;

        public GotoPair(int Tid, String Tscope) {
            this.Tid = Tid;
            this.Tscope = Tscope;
        }

        public void setTid(int tid) {
            Tid = tid;
        }

        public void setTscope(String tscope) {
            Tscope = tscope;
        }
}
